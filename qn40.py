# calculator

a = int(input("enter first number :"))
b = int(input("enter second  number :"))


def sum(i,j):
    return i+j

def product(i,j):
    return i*j

def quotient(i,j):
    return i/j

def difference(i,j):
    return i-j

print("sum is :", sum(a,b))
print("product is :", product(a,b))
print("quotient is :", quotient(a,b))
print("difference is :", difference(a,b))
